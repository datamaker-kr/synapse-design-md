/**
 * findings-to-issues.mjs
 *
 * Parse a `<component>.findings.md` and emit one issue-ready markdown file
 * per gap into evidence/issues/<component>/. Does NOT create GitHub or Linear
 * issues — that is a visible, shared-state action that requires explicit user
 * authorization. The output files are ready to pipe into `gh issue create -F`
 * or paste into Linear.
 *
 *   synapse-design-md contract issues \
 *     --findings examples/molecules/nav-item.findings.md
 *     [--out evidence/issues] [--labels design-debt,a11y]
 *
 * The parser walks the document and groups by `## Gap #N — title` headings.
 * Each gap's body becomes the issue body verbatim, with metadata (component
 * name, source file, labels) added as YAML frontmatter.
 */

import fs from "node:fs/promises";
import path from "node:path";

export async function findingsToIssues(options = {}) {
  const findingsPath = options.findings;
  if (!findingsPath) {
    throw new Error("Usage: synapse-design-md contract issues --findings <path> [--out <dir>] [--labels <a,b,c>]");
  }
  const findingsAbs = path.resolve(process.cwd(), findingsPath);
  const outRoot = path.resolve(process.cwd(), options.out || "evidence/issues");
  const labels = (options.labels || "design-debt").split(",").map((s) => s.trim()).filter(Boolean);

  const text = await fs.readFile(findingsAbs, "utf8");
  const component = inferComponentName(findingsAbs);
  const gaps = parseGaps(text);
  if (gaps.length === 0) {
    console.log("No gap sections found (expected '## Gap #N — title' headings).");
    return [];
  }

  const outDir = path.join(outRoot, component);
  await fs.mkdir(outDir, { recursive: true });

  const written = [];
  for (const gap of gaps) {
    const slug = slugify(`${gap.id}-${gap.title}`);
    const outPath = path.join(outDir, `${slug}.md`);
    const body = renderIssue({ gap, component, findings: path.relative(process.cwd(), findingsAbs), labels });
    await fs.writeFile(outPath, body);
    written.push({ id: gap.id, title: gap.title, path: outPath });
  }

  console.log(`Wrote ${written.length} issue file(s) under ${path.relative(process.cwd(), outDir)}/`);
  for (const w of written) {
    console.log(`  ${w.id}  ${w.title}`);
    console.log(`    ${path.relative(process.cwd(), w.path)}`);
  }
  console.log(`\nTo create the issues on GitHub:`);
  console.log(`  for f in ${path.relative(process.cwd(), outDir)}/*.md; do gh issue create -F "$f"; done`);
  console.log(`\nFor Linear: paste each file's body into a new issue. The frontmatter doubles as a metadata cheatsheet.`);
  return written;
}

function inferComponentName(findingsPath) {
  const base = path.basename(findingsPath, ".findings.md");
  return base || "component";
}

function parseGaps(text) {
  const lines = text.split("\n");
  const gaps = [];
  let current = null;
  for (const line of lines) {
    const m = line.match(/^##\s+Gap\s+(#\d+)\s+[—\-:]\s*(.+?)\s*$/);
    if (m) {
      if (current) gaps.push(current);
      current = { id: m[1], title: m[2], lines: [] };
      continue;
    }
    if (!current) continue;
    // A non-gap top-level heading ends the section.
    if (/^##\s+(?!Gap\s+#)/.test(line) || /^---\s*$/.test(line)) {
      gaps.push(current);
      current = null;
      continue;
    }
    current.lines.push(line);
  }
  if (current) gaps.push(current);
  return gaps;
}

function renderIssue({ gap, component, findings, labels }) {
  const title = `[${component}] ${gap.id} — ${gap.title}`;
  const body = gap.lines.join("\n").replace(/^\n+/, "").replace(/\n+$/, "");
  return `---
title: ${escapeYaml(title)}
labels: [${labels.map((l) => `"${l}"`).join(", ")}]
component: ${component}
gap: ${gap.id}
source: ${findings}
---

# ${title}

${body}

---

_Generated by \`synapse-design-md contract issues\` from \`${findings}\`._
`;
}

function escapeYaml(s) {
  return `"${s.replace(/"/g, '\\"')}"`;
}

function slugify(s) {
  return s
    .toLowerCase()
    .replace(/#/g, "")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}
